<section class="section">
	<h2>Instructions</h2>
	<p>
		This is a browsergame based on the <a
			href="https://twistypuzzles.com/cgi-bin/puzzle.cgi?pkey=889"
			target="_blank">Brainball puzzle</a
		>. Pieces with numbers 1 to 13 are arranged on a ring around a ball,
		with white on one side and orange on the other.
	</p>
	<p>
		The ring can be rotated, and the pieces attached to the black parts of
		the ball can be flipped. To do this, use the corresponding buttons. Play
		around to get a feeling how the puzzle works.
	</p>
	<p>
		After scrambling the puzzle, the goal is to arrange the pieces in such a
		way that the white side is facing up and the numbers are in the correct
		order from 1 to 13.
	</p>
</section>
